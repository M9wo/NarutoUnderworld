  public void generateScentParticles(Entity theEntity, float red, float green, float blue) {
    double motionX = theEntity.worldObj.rand.nextGaussian() * 0.02D;
    double motionY = theEntity.worldObj.rand.nextGaussian() * 0.02D;
    double motionZ = theEntity.worldObj.rand.nextGaussian() * 0.02D;
    ByakuganTrack byakuganTrack = new ByakuganTrack(theEntity.worldObj, theEntity.posX + (theEntity.worldObj.rand.nextFloat() * theEntity.width * 2.0F) - theEntity.width, theEntity.posY + (theEntity.worldObj.rand.nextFloat() * theEntity.height), theEntity.posZ + (theEntity.worldObj.rand.nextFloat() * theEntity.width * 2.0F) - theEntity.width, motionX, motionY, motionZ, red, green, blue);
    (Minecraft.getMinecraft()).effectRenderer.addEffect((EntityFX)byakuganTrack);
  }
